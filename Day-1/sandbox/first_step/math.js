// math.js - Module with math operations

// Addition function
const sum = (a, b) => a + b;

// Subtraction function
const diff = (a, b) => a - b;

// Multiplication function
const prod = (a, b) => a * b;

// Division function
const quot = (a, b) => a / b;

// Export all functions
module.exports = {
  sum,
  diff,
  prod,
  quot
};